package com.cytx.pojo;

public class Permission {
    private Integer permissionId;

    private Integer permissionUserId;

    private Integer permissionRole;

    public Integer getPermissionId() {
        return permissionId;
    }

    public void setPermissionId(Integer permissionId) {
        this.permissionId = permissionId;
    }

    public Integer getPermissionUserId() {
        return permissionUserId;
    }

    public void setPermissionUserId(Integer permissionUserId) {
        this.permissionUserId = permissionUserId;
    }

    public Integer getPermissionRole() {
        return permissionRole;
    }

    public void setPermissionRole(Integer permissionRole) {
        this.permissionRole = permissionRole;
    }
}